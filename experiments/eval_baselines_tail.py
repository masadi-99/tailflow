"""
Run simple baselines (seasonal naive, empirical bootstrap) on the same test
splits used by eval_tail_metrics.py and score them with our tail_metrics
module. Produces a JSON row that can be pasted directly into the comparison
table next to Phase-2 / Self-train-R2.

Seasonal naive: forecast y_{t+k} = y_{t+k-P} where P is the seasonal period
(24 for hourly, 5 for business-daily). 200 samples are generated by adding
zero-mean Gaussian noise with per-window std estimated from the training
residuals of the last seasonal block — a standard benchmark.

Empirical bootstrap: sample 200 draws from the last `pred_len * K` window of
the context with replacement — a sanity floor that has no seasonality model.
"""
import os, sys, json, argparse, logging, time
import numpy as np

from gluonts.dataset.repository.datasets import get_dataset
from gluonts.time_feature import time_features_from_frequency_str
from gluonts.transform import (
    AddObservedValuesIndicator, AddTimeFeatures, AsNumpyArray,
    Chain, InstanceSplitter, TestSplitSampler,
)

sys.path.insert(0, os.path.join(os.path.dirname(__file__), '..'))
from meanflow_ts.tail_metrics import compute_all_tail_metrics, compute_train_thresholds

logging.basicConfig(format="%(asctime)s | %(message)s", level=logging.INFO)
logger = logging.getLogger(__name__)

CONFIGS = {
    "electricity_nips":              {"freq": "H", "ctx": 24, "pred": 24, "period": 24},
    "solar_nips":                    {"freq": "H", "ctx": 24, "pred": 24, "period": 24},
    "traffic_nips":                  {"freq": "H", "ctx": 24, "pred": 24, "period": 24},
    "exchange_rate_nips":            {"freq": "B", "ctx": 30, "pred": 30, "period": 5},
    "kdd_cup_2018_without_missing":  {"freq": "H", "ctx": 48, "pred": 48, "period": 24},
    "uber_tlc_hourly":               {"freq": "H", "ctx": 48, "pred": 24, "period": 168},
}
LAG_MAP = {"H": 672, "B": 750}


def iter_test_windows(name, cfg):
    """Yield (past, future) arrays. GluonTS test set: last pred_len is target."""
    ds = get_dataset(name)
    pred_len = cfg["pred"]
    for e in ds.test:
        full = np.asarray(e["target"], dtype=np.float32)
        if full.ndim == 2:
            full = full[0]
        if len(full) <= pred_len:
            continue
        past = full[:-pred_len]
        future = full[-pred_len:]
        yield past, future


def seasonal_naive_samples(past, pred_len, period, n_samples, rng):
    """
    Mean forecast: y_{t+k} = y_{t+k-period} tiled over pred_len steps.
    Sigma: std of seasonal-lag residuals over the last 3 seasonal blocks.
    Samples: gaussian noise around the mean.
    """
    base = past[-period:] if pred_len <= period else \
        np.tile(past[-period:], (pred_len + period - 1) // period)[:pred_len]
    mean = base[:pred_len]

    # Seasonal residual std from the last 3*period steps
    recent = past[-3 * period:]
    if len(recent) >= 2 * period:
        shift = recent[period:] - recent[:-period]
        sigma = float(np.std(shift)) if shift.size else 0.0
    else:
        sigma = float(np.std(past[-period:])) if period <= len(past) else 1.0
    if sigma <= 0:
        sigma = max(float(np.std(past[-period:])), 1e-6)

    noise = rng.standard_normal((n_samples, pred_len)) * sigma
    return mean[None, :] + noise


def bootstrap_samples(past, pred_len, n_samples, rng):
    """Sample with replacement from the last `pred_len * 4` positions."""
    pool = past[-pred_len * 4:]
    if len(pool) < pred_len:
        pool = past[-pred_len:]
    idx = rng.integers(0, len(pool), size=(n_samples, pred_len))
    return pool[idx]


def eval_baseline(name, method, num_samples=200, seed=0):
    cfg = CONFIGS[name]
    rng = np.random.default_rng(seed)
    pred_len = cfg["pred"]
    period = cfg["period"]

    ds = get_dataset(name)
    train_thr = compute_train_thresholds(ds.train, quantiles=(0.9, 0.95, 0.99))

    samples_list = []
    targets_list = []
    for past, future in iter_test_windows(name, cfg):
        if method == "seasonal_naive":
            s = seasonal_naive_samples(past, pred_len, period, num_samples, rng)
        elif method == "bootstrap":
            s = bootstrap_samples(past, pred_len, num_samples, rng)
        else:
            raise ValueError(method)
        samples_list.append(s)
        targets_list.append(future)

    samples = np.stack(samples_list, axis=0)  # (N, S, T)
    targets = np.stack(targets_list, axis=0)  # (N, T)
    t0 = time.time()
    m = compute_all_tail_metrics(samples, targets, train_thresholds=train_thr)
    m["method"] = method
    m["num_windows"] = len(samples_list)
    logger.info(f"[{name}/{method}] N={len(samples_list)}  crps={m['crps']:.4f}  "
                f"twCRPS95={m['twCRPS_q095']:.4f}  twCRPS99={m['twCRPS_q099']:.4f}  "
                f"wqloss95={m['wqloss_q095']:.4f}  cov90={m['coverage_90']:.3f}  "
                f"({time.time()-t0:.1f}s)")
    return m


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--datasets", nargs="+",
                   default=["solar_nips", "electricity_nips", "traffic_nips",
                            "exchange_rate_nips"])
    p.add_argument("--num-samples", type=int, default=200)
    p.add_argument("--output", default="results_baselines_tail.json")
    args = p.parse_args()

    out = {"baselines": {}}
    for name in args.datasets:
        out["baselines"][name] = {}
        for method in ["seasonal_naive", "bootstrap"]:
            out["baselines"][name][method] = eval_baseline(
                name, method, num_samples=args.num_samples)
            with open(args.output, "w") as f:
                json.dump(out, f, indent=2, default=float)
    logger.info(f"Wrote {args.output}")


if __name__ == "__main__":
    main()
